  # POST /games
  # POST /games.json
  def create(player_id)
    player = Player.find(player_id)
    game_request = Lol::CurrentGameRequest.new(Player::API_KEY, player.region)
    game_info = game_request.spectator_game_info(platform_id, player.summoner_id)
    @game.mode = game_info.game_mode
    @game.start = DateTime.strptime(game_info.game_start_time.to_s,'%s')

    respond_to do |format|
      if @game.save
        format.html { redirect_to @game, notice: 'Game was successfully created.' }
        format.json { render :show, status: :created, location: @game }
      else
        format.html { render :new }
        format.json { render json: @game.errors, status: :unprocessable_entity }
      end
    end
  end

  # PATCH/PUT /games/1
  # PATCH/PUT /games/1.json
  def update
    respond_to do |format|
      if @game.update(game_params)
        format.html { redirect_to @game, notice: 'Game was successfully updated.' }
        format.json { render :show, status: :ok, location: @game }
      else
        format.html { render :edit }
        format.json { render json: @game.errors, status: :unprocessable_entity }
      end
    end
  end

